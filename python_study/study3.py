# 변수에 값을 넣는 것 => 할당

# sum = 1 + 2 => 1+2의 계싼된 결과를 sum이라는 변수에 할당한다.
# sum은 변수명이고 "="은 같다는 의미가 아니라 할당(assigenment)이라고 한다.

# "=" : 할당 연산자
# "+" : 산술 연산자

# "+=" : 산술 후 바로 할당하는 문법
# /=, -=, *= 모두 가능


# 변수를 사용하면서 사용 가능한 문법

sum = 1 + 2
sum = sum + 3
sum = sum + 4
sum = sum + 5

print(sum)

sum2 = 1 + 2
sum2 += 3
sum2 += 4
sum2 += 5

print(sum2)