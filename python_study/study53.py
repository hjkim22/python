# 조건문 (True, False)

if True:
    print(True)
else:
    print(False)

if False:
    print(True)
else:
    print(False)

if None:
    print(True)
else:
    print(False)

if "hello":
    print(True)
else:
    print(False)

if '':
    print(True)
else:
    print(False)

if not True:
    print(True)
else:
    print(False)